struct WorkerMonocleData
{
	mon::PortalPair pp;
	mon::TeleportChainParams paramsTemplate; // paramsTemplate.pp points to pp

	WorkerMonocleData(const Vector& bluePos,
	                  const QAngle& blueAng,
	                  const Vector& orangePos,
	                  const QAngle& orangeAng,
	                  mon::PlacementOrder order,
	                  mon::GameVersion gv,
	                  const mon::TeleportChainParams& paramsTemplate)
	    : pp(bluePos, blueAng, orangePos, orangeAng, order, gv), paramsTemplate(paramsTemplate)
	{
		this->paramsTemplate.pp = &pp;
	}
};

struct WorkerPixel
{
	uint16_t x, y;

	bool operator==(WorkerPixel o) const
	{
		return x == o.x && y == o.y;
	}
};

struct WorkerPixelData
{
	uint8_t tpExceeded : 1;
	uint8_t endBehindPortal : 1;
	int8_t cumTp : 6;

	constexpr MonocleImageColorType ToColor() const
	{
		if (tpExceeded)
			return MIC_TP_EXCEEDED;
		switch (cumTp)
		{
		case 0:
			return endBehindPortal ? MIC_0CUM_BEHIND : MIC_0CUM_FRONT;
		case 1:
			return endBehindPortal ? MIC_1CUM_BEHIND : MIC_1CUM_FRONT;
		case -1:
			return MIC_NEG1CUM;
		case 2:
			return MIC_2CUM;
		case -2:
			return MIC_NEG2CUM;
		case 3:
			return MIC_3CUM;
		case -3:
			return MIC_NEG3CUM;
		default:
			return MIC_MISC;
		}
	}
};

/*
* Runs monocle for each pixel on a separate thread. The worker thread writes the monocle result to
* an "image", and the render (main) thread periodically uploads rows of that image to a dx9
* texture. State diagram:
* 
* +-------------+  +-------+  +----+
* |Uninitialized|->|Working|->|Idle|
* +-------------+  +-------+  +----+
*                   ^     |    |
*                   |     v    v
*                   +--RestartWork()
* 
* You can grab information about the job only once RestartWork() has been called at least once.
*/
class MonocleWorker
{
public:
private:
	std::shared_ptr<const WorkerMonocleData> monocleData;
	std::thread thread;
	std::mutex mtx;
	std::condition_variable cvWorker;
	std::condition_variable cvMain;
	bool requestPause = true;
	bool requestStop = false;
	bool paused = false;

	// the monocle result generated by the worker thread
	struct
	{
		std::unique_ptr<WorkerPixelData[]> pxls;
		WorkerPixel size;
		std::atomic<uint16_t> processedRows = 0;

		/*
		* A quad in pixel space that represents a set outside of which it is normally not possible
		* to enter this portal (e.g. the pixel is very close to the portal edge). This data is not
		* needed by the worker but it's the best place to put it.
		*/
		std::array<Vector2D, 4> effectiveTpSpace;
	} img;

	// the actual texture displayed in imgui
	struct
	{
		ComPtr<IDirect3DTexture9> texture = nullptr;
		uint16_t nRowsUploaded = 0;
	} tex;

public:
	MonocleWorker();
	~MonocleWorker();

	auto& GetMonocleData()
	{
		return monocleData;
	}

	auto& GetMonocleData() const
	{
		return monocleData;
	}

	auto& GetEffectiveTpSpace() const
	{
		return img.effectiveTpSpace;
	}

	void RestartWork(std::shared_ptr<WorkerMonocleData> optNewMonocleData, WorkerPixel newImgSize);

	void MarkTextureDirty()
	{
		tex.nRowsUploaded = 0;
	}

	ComPtr<IDirect3DTexture9> UpdateAndGetTexture();

	WorkerPixel GetImageSize() const
	{
		return img.size;
	}

	Vector PixelToWorldCoordinates(WorkerPixel px, const mon::TeleportChainParams* paramsOverride = nullptr) const;

	std::optional<WorkerPixelData> GetPixelResult(WorkerPixel px) const
	{
		if (!img.pxls || img.processedRows < px.y || !monocleData)
			return {};
		return img.pxls[px.y * img.size.x + px.x];
	}

	WorkerPixelData DoWorkForPixel(WorkerPixel px,
	                               const mon::TeleportChainParams& paramsTemplate,
	                               mon::TeleportChainResult& result) const;

	static std::optional<bool> EndBehindPortal(const mon::TeleportChainParams& params,
	                                           const mon::TeleportChainResult& result);

private:
	void SetupTexture();
	void PauseWorker();
	void WorkerLoop();
	void RecomputeEffectiveTpSpace();
};

