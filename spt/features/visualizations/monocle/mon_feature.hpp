struct d3d9_argb
{
	unsigned char b, g, r, a;

	explicit operator color32() const
	{
		return color32{.r = r, .g = g, .b = b, .a = a};
	}

	static d3d9_argb FromColor32(color32 c)
	{
		return d3d9_argb{.b = c.b, .g = c.g, .r = c.r, .a = c.a};
	}
};

